      <p><h3><strong style="color:#0467bd;font-family:'Montserrat Medium'">Objetivo General</strong></h3></p>
      <p><h5 style="color:#000000;font-family:'Montserrat Medium'">Formar profesionistas en Ingeniería Ambiental éticos, analíticos, críticos y creativos con las competencias para identificar, 
    proponer y resolver problemas ambientales de manera multidisciplinaria, asegurando la protección, conservación y mejoramiento del ambiente, 
    bajo un marco legal, buscando el desarrollo sustentable en beneficio de la vida en el planeta.</h5></p>
<br>
<p><h3><strong style="color:#0467bd;font-family:'Montserrat Medium'">Perfil de Ingreso</strong></h3></p>

<p><h5 style="color:#000000;font-family:'Montserrat Medium'">El  estudiante deberá tener habilidades para la comunicación oral y escrita, habilidades matemáticas de nivel medio superior, capacidad de redacción ante diferentes situaciones, 
con actitud proactiva y responsable, destreza para analizar y sintetizar información. Tener iniciativa y contar con la capacidad para establecer relaciones interpersonales,
nivel básico de inglés, trabajo en equipo y apertura para conocer otras culturas</h5></p>
<br>

<p><h3><strong style="color:#0467bd;font-family:'Montserrat Medium'">Objetivos Educacionales</strong></h3></p>

<p><h5 style="color:#000000;font-family:'Montserrat Medium'"><li>Participar en la planeación y ejecución de proyectos que se relacionan con el cuidado del medio ambiente.</li>
<li>Emplear tecnología para el tratamiento y control de contaminantes.</li>
<li>Aplicar la legislación ambiental vigente en la solución de problemas ambientales.</li>
<li>Promover la sostenibilidad a través de aplicaciones, páginas web o talleres de educación ambiental.</li></h5></p>
<br>

<p><h3><strong style="color:#0467bd;font-family:'Montserrat Medium'">Campo Ocupacional</strong></h3></p>
<p><h5 style="color:#000000;font-family:'Montserrat Medium'">El ingeniero Ambiental ejercerá su profesión en la investigación para el estudio del impacto ambiental, en la fabricación de nuevos productos en la industria farmacéutica, 
alimentaria y de producción, puede crear su propia empresa ofreciendo servicio de consultoría y gestión ambiental.</h5></p>
<br>

<p><h3><strong style="color:#0467bd;font-family:'Montserrat Medium'">Especialidad Vigente</strong></h3></p>

<p><h5 style="color:#000000;font-family:'Montserrat Medium'">OBJETIVO
<br> Formar especialistas que brinden soluciones sustentables, éticas e innovadores en materia de manejo, tratamiento y restauración de sistemas hídricos, bajo la normatividad aplicable.
<br> PLAN DE ESTUDIOS.
<br> IAMB-2010-206. ESPECIALIDAD VIGENTE: Gestion y tratamiento del agua IAMB-GTA-2022-03.</h5></p>
<br>

<p><h3><strong style="color:#0467bd;font-family:'Montserrat Medium'">Atributos de Egreso</strong></h3></p>

<p><h5 style="color:#000000;font-family:'Montserrat Medium'"><li>Vincula el valor de los recursos naturales para promover su uso sustentable de acuerdo a las necesidades de la región, mediante instrumentos de concientización, sensibilización y comunicación.</li>
        <li>Participa en el desarrollo y ejecución del protocolo de investigación básica o aplicada para la resolución de problemas ambientales.</li>
        <li>Elabora, implementa y mantiene sistemas de gestión ambiental.</li>
        <li>Participa en la realización de auditorías ambientales en el sector público y privado.</li>
        <li>Realiza diagnósticos y evaluaciones de impacto y riesgo ambiental sustentados en métodos y procedimientos certificados conforme a los criterios Nacionales e Internacionales.</li>
        <li>Elabora estudios de factibilidad económica y técnica de los procesos para la prevención y control ambiental.</li>
        <li>Propone e innova tecnologías para el manejo de los residuos cumpliendo la legislación ambiental vigente.</li>
        <li>Conoce y aplica criterios de ingeniería básica y aplicada, así como de las ciencias biológicas para el dimensionamiento, adecuación, operación, mantenimiento y desarrollo de tecnologías de tratamiento, prevención, control y transformación de efluentes sólidos, líquidos y gaseosos contaminados.</li>
        <li>Conoce y aplica las TIC, así como sistemas computacionales o software especializados en el área ambiental.</li>
        <li>Es analítico, ético, critico, y consiente de la importancia de su entorno para la vida y respetuoso de la misma, siendo promotor del desarrollo sustentable.</li>
        <li>Es capaz de formar recursos humanos, realizar actividades de docencia, investigación y capacitación</li>
        <li>Tiene una actitud emprendedora y de liderazgo para interactuar con grupos multidisciplinarios e interdisciplinarios en la búsqueda de soluciones a los problemas del deterioro del medio ambiente.</li></h5></p>
